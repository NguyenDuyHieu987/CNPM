    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {

            if (txtMaGiamGia.getText().equals("") || txtTen.getText().equals("") || (Integer) spinnerPhanTram.getValue() == 0
                    || txtHieuLuc.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
            } else {

                List<MaGiamGia> maGiamGias = giamGiaController.GetAllMaGiamGia();
                for (MaGiamGia maGiamGia : maGiamGias) {
                    if (txtMaGiamGia.getText().equals(maGiamGia.getMaGiamGia())) {
                        JOptionPane.showMessageDialog(null, "Mã giảm giá đã tồn tại", "Thông báo", JOptionPane.WARNING_MESSAGE);
                        txtMaGiamGia.requestFocus();
                        return;
                    }
                }
                int confirm = JOptionPane.showConfirmDialog(null, "Bạn có muốn thêm mã giảm giá " + txtMaGiamGia.getText() + " không?", "Thông báo", JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    MaGiamGia maGiamGia = new MaGiamGia();

                    maGiamGia.setMaGiamGia(txtMaGiamGia.getText());
                    maGiamGia.setTen(txtTen.getText());
                    maGiamGia.setPhanTram((Integer) spinnerPhanTram.getValue());
                    maGiamGia.setHieuLuc(txtHieuLuc.getText());

                    boolean check = giamGiaController.ThemMaGiamGia(maGiamGia);
                    if (check == true) {
                        JOptionPane.showMessageDialog(null, "Thêm mới thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        //                defaultTableModel.setRowCount(0);
                        clearDataTable();
                        LoadDataTable();
                        ClearTextField();
                        txtMaGiamGia.requestFocus();

                    }
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Thêm mới thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnThemActionPerformed


    public void InsertDataTable() {
        giamGiaController = new GiamGiaController();
        linhKienController = new LinhKienController();

        defaultTableModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tbMaGiamGia.setModel(defaultTableModel);
        defaultTableModel.addColumn("Mã giảm giá");
        defaultTableModel.addColumn("Tên");
        defaultTableModel.addColumn("Phần trăm");
        defaultTableModel.addColumn("Hiệu lực");

//        tbLinhKien.getColumnModel().getColumn(3).setPreferredWidth(300);
        LoadDataTable();
        txtMaGiamGia.requestFocus();
    }

    public void LoadDataTable() {
        cbMaGiamGiaSearch.removeAllItems();

        List<MaGiamGia> maGiamGias = giamGiaController.GetAllMaGiamGia();

        for (MaGiamGia giamGia : maGiamGias) {
            defaultTableModel.addRow(new Object[]{
                giamGia.getMaGiamGia(),
                giamGia.getTen(),
                giamGia.getPhanTram(),
                giamGia.getHieuLuc(),});

            cbMaGiamGiaSearch.addItem(giamGia.getMaGiamGia());
        }

    }

    public void clearDataTable() {
        while (defaultTableModel.getRowCount() > 0) {
            defaultTableModel.removeRow(0);
        }
    }

    public void ClearTextField() {
        txtMaGiamGia.setText(null);
        txtTen.setText(null);
        spinnerPhanTram.setValue(0);
        txtHieuLuc.setText(null);

    }


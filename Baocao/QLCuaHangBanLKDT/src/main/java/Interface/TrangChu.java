    private void menuQLLKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuQLLKActionPerformed
        // TODO add your handling code here:

        JButton invoker = (JButton) PopupMenuPhong.getInvoker();
        String CurrentBtn = invoker.getText();

        this.dispose();

        if (cbHang.getSelectedItem() == "Tất cả") {
            frmQLLinhKien fQLLinhKien = new frmQLLinhKien(TaiKhoan, MatKhau, CurrentBtn, null);
            fQLLinhKien.setLocationRelativeTo(this);
            fQLLinhKien.setVisible(true);
        } else {
            frmQLLinhKien fQLLinhKien = new frmQLLinhKien(TaiKhoan, MatKhau, CurrentBtn, cbHang.getSelectedItem().toString());
            fQLLinhKien.setLocationRelativeTo(this);
            fQLLinhKien.setVisible(true);
        }

    }//GEN-LAST:event_menuQLLKActionPerformed


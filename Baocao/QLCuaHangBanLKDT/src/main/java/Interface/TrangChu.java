    public void PopupMenu() {
        Component[] component = ListPhong.getComponents();
        for (int i = 0; i < component.length; i++) {
            if (component[i] instanceof JButton) {
                JButton button = (JButton) component[i];
                String CurrentBtn = button.getText();

                button.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        dispose();
                        if (cbHang.getSelectedItem() == "Tất cả") {
                            frmThanhToan fThanhToan = new frmThanhToan(TaiKhoan, MatKhau, CurrentBtn, null);
                            fThanhToan.setLocationRelativeTo(null);
                            fThanhToan.setVisible(true);
                        } else {
                            frmThanhToan fThanhToan = new frmThanhToan(TaiKhoan, MatKhau, CurrentBtn, cbHang.getSelectedItem().toString());
                            fThanhToan.setLocationRelativeTo(null);
                            fThanhToan.setVisible(true);
                        }

                    }

                });

                button.addMouseListener(new MouseListener() {

                    @Override
                    public void mouseClicked(java.awt.event.MouseEvent e) {

                    }

                    @Override
                    public void mousePressed(java.awt.event.MouseEvent e) {
                        if (SwingUtilities.isRightMouseButton(e)) { // if right click

                            button.setComponentPopupMenu(PopupMenuPhong);

                        }
                    }

                    @Override
                    public void mouseReleased(java.awt.event.MouseEvent e) {
                    }

                    @Override
                    public void mouseEntered(java.awt.event.MouseEvent e) {
                    }

                    @Override
                    public void mouseExited(java.awt.event.MouseEvent e) {
                    }
                });
            }

        }

    }

    public void LoadData() {
        dangNhapController = new DangNhapController();
        linhKienController = new LinhKienController();
        cbHang.removeAllItems();

        List<String> listHang = linhKienController.GetAllHang();

        cbHang.addItem("Tất cả");

        for (String hang : listHang) {
            cbHang.addItem(hang);
        }

        if (dangNhapController.GetRoleCurrentAcount(TaiKhoan, MatKhau).equals("ADMIN")) {
            btnStatistic.setEnabled(true);
        } else if (dangNhapController.GetRoleCurrentAcount(TaiKhoan, MatKhau).equals("USER")) {
            btnStatistic.setEnabled(false);

        }

    }

